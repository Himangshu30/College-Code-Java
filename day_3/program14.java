// Use of break statement in switch
public class program14{
	public static void main (String[] args) {
	
	int n = 2;
	
	switch(n){
		case 1:
		System.out.println("Himangshu");
		break;
		case 2:
		System.out.println("Ankit");
		break;
		default:
		System.out.println("default case");
	         }
	}
}
